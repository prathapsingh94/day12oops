package com.onesoft.day10;

public class Wood {
	private String type;
	private int price;
	private String color;
	
	public void setType(String type) {
		this.type=type;
	}
	public String getType() {
		return type;
	}
	public void setPrice(int price) {
		this.price=price;
	}
	public int getPrice() {
		return price;
	}
	public void setColor(String color) {
		this.color=color;
	}
	public String getColor() {
		return color;
	}

}
