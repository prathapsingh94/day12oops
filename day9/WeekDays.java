package com.onesoft.day9;

public class WeekDays {
	public void findDay (String day) {
		switch (day) {
		case "sunday": System.out.println("it is first day");break;
		case "monday": System.out.println("it is second day");break;
		case "tuesday": System.out.println("it is third day");break;
		case "wednesday": System.out.println("it is fourth day");break;
		case "thrusday": System.out.println("it is fifth day");break;
		case "friday": System.out.println("it is sixth day");break;
		case "saturday": System.out.println("it is seventh");break;
		default: System.out.println("Invalid");break;
		
		}
	}	
}
